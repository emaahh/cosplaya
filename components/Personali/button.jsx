"use client"
import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva } from "class-variance-authority";

import { cn } from "@/lib/utils"

import chroma from "chroma-js"

const buttonVariants = cva(
  "inline-flex items-center justify-center whitespace-nowrap rounded-md text-sm font-medium ring-offset-background transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/90",
        destructive:
          "bg-destructive text-destructive-foreground hover:bg-destructive/90",
        outline:
          "border border-input bg-background hover:bg-accent hover:text-accent-foreground",
        secondary:
          "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost: "hover:bg-accent hover:text-accent-foreground",
        link: "text-primary underline-offset-4 hover:underline",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3",
        lg: "h-11 rounded-md px-8",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

const Button = React.forwardRef(({ className, variant, size, adjustColor='#000000', asChild = false, ...props}, ref) => {
  const Comp = asChild ? Slot : "button"
  return (
    (<Comp
      className={cn(buttonVariants({ variant, size, className }))}
      ref={ref}

      //gestore colore custom
      style={{backgroundColor: adjustColor, color: chroma(adjustColor).luminance() < 0.025? chroma(adjustColor).brighten(4) : chroma(adjustColor).brighten(4)}} 
      onMouseEnter={e => {e.target.style.backgroundColor = `${chroma(adjustColor).luminance() < 0.025? chroma(adjustColor).brighten(): chroma(adjustColor).darken()}`}} 
      onMouseLeave={e => {e.target.style.backgroundColor = `${adjustColor}`}}

      {...props} />)
  );
})
Button.displayName = "Button"

export { Button, buttonVariants }
